"use client";

import { useEffect, useState } from "react";
import Link from "next/link";
import { useRouter } from "next/navigation";
import { ChevronLeft, Download, Sparkles } from "lucide-react";
import { ResultsDisplay } from "@/components/ResultsDisplay";
import { LoadingSpinner } from "@/components/LoadingSpinner";
import { AnalysisResponse } from "@/types";

export default function ResultsPage() {
  const router = useRouter();
  const [result, setResult] = useState<AnalysisResponse | null>(null);
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    // Get result from session storage
    const storedResult = sessionStorage.getItem("analysisResult");
    if (storedResult) {
      try {
        setResult(JSON.parse(storedResult));
      } catch (err) {
        console.error("Failed to parse stored result:", err);
      }
    }
    setIsLoading(false);
  }, []);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <LoadingSpinner />
      </div>
    );
  }

  if (!result) {
    return (
      <div>
        <nav className="border-b border-gray-200 bg-white">
          <div className="max-w-6xl mx-auto px-6 py-4 flex items-center justify-between">
            <Link
              href="/analyze"
              className="flex items-center gap-2 text-gray-600 hover:text-primary transition-colors"
            >
              <ChevronLeft className="w-5 h-5" />
              Back
            </Link>
            <div className="flex items-center gap-2">
              <Sparkles className="w-6 h-6 text-primary" />
              <span className="font-semibold text-gray-900">
                AI Interview Agent
              </span>
            </div>
            <div className="w-20"></div>
          </div>
        </nav>

        <div className="max-w-4xl mx-auto px-6 py-12 text-center">
          <h1 className="text-2xl font-bold text-gray-900 mb-4">
            No results found
          </h1>
          <p className="text-gray-600 mb-6">
            Please go back and run an analysis first
          </p>
          <Link
            href="/analyze"
            className="inline-block px-6 py-2 bg-primary text-white rounded-lg hover:bg-primary/90 transition-colors"
          >
            Run Analysis
          </Link>
        </div>
      </div>
    );
  }

  const handleDownloadResults = () => {
    const resultsText = `
AI INTERVIEW AGENT - ANALYSIS RESULTS
=====================================

MATCH SCORE: ${result.matchScore}%

STRENGTHS:
${result.strengths.map((s, i) => `${i + 1}. ${s}`).join("\n")}

SKILLS TO DEVELOP:
${result.missingSkills.map((s, i) => `${i + 1}. ${s}`).join("\n")}

RECOMMENDED LEARNING TOPICS:
${result.recommendedTopics.map((t, i) => `${i + 1}. ${t}`).join("\n")}

LIKELY INTERVIEW QUESTIONS:
${result.interviewQuestions.map((q, i) => `${i + 1}. ${q}`).join("\n\n")}

Generated by AI Interview Agent
`;

    const element = document.createElement("a");
    element.setAttribute("href", "data:text/plain;charset=utf-8," + encodeURIComponent(resultsText));
    element.setAttribute("download", "interview-analysis-results.txt");
    element.style.display = "none";
    document.body.appendChild(element);
    element.click();
    document.body.removeChild(element);
  };

  return (
    <div>
      {/* Navigation */}
      <nav className="border-b border-gray-200 bg-white">
        <div className="max-w-6xl mx-auto px-6 py-4 flex items-center justify-between">
          <Link
            href="/analyze"
            className="flex items-center gap-2 text-gray-600 hover:text-primary transition-colors"
          >
            <ChevronLeft className="w-5 h-5" />
            Back
          </Link>
          <div className="flex items-center gap-2">
            <Sparkles className="w-6 h-6 text-primary" />
            <span className="font-semibold text-gray-900">
              AI Interview Agent
            </span>
          </div>
          <button
            onClick={handleDownloadResults}
            className="flex items-center gap-2 px-4 py-2 bg-primary/10 text-primary hover:bg-primary/20 rounded-lg transition-colors font-medium"
          >
            <Download className="w-4 h-4" />
            Download
          </button>
        </div>
      </nav>

      {/* Content */}
      <div className="max-w-6xl mx-auto px-6 py-12">
        <div className="mb-12">
          <h1 className="text-4xl font-bold text-gray-900 mb-3">
            Analysis Results
          </h1>
          <p className="text-gray-600 text-lg">
            Your personalized resume analysis and interview preparation guide
          </p>
        </div>

        <ResultsDisplay data={result} />

        {/* CTA */}
        <div className="mt-16 bg-gradient-to-r from-primary/10 to-blue-500/10 rounded-xl p-8 text-center border border-primary/20">
          <h3 className="text-2xl font-bold text-gray-900 mb-4">
            Ready for Your Interview?
          </h3>
          <p className="text-gray-600 mb-6 max-w-2xl mx-auto">
            Use these insights to prepare and practice answering the interview
            questions. Focus on the missing skills and recommended topics to
            strengthen your candidacy.
          </p>
          <div className="flex gap-4 justify-center flex-wrap">
            <Link
              href="/analyze"
              className="px-6 py-3 bg-primary text-white rounded-lg hover:bg-primary/90 transition-colors font-medium"
            >
              Analyze Another Resume
            </Link>
            <Link
              href="/"
              className="px-6 py-3 border border-gray-300 text-gray-900 rounded-lg hover:bg-gray-50 transition-colors font-medium"
            >
              Home
            </Link>
          </div>
        </div>
      </div>
    </div>
  );
}
